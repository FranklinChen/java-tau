package com.franklinchen.math;

/**
   Useful mathematical constants.
*/
public final class Constants {
    /**
       The fundamental mathematical constant, τ.

       You may be familiar with the derived constant, pi, π = τ/2 = 3.1415...
       pi is a historical mistake.

       @see <a href="http://tauday.com/">The Tau Manifesto</a>
     */
    public static final double TAU = 6.28318530717958647692;

    /**
       Unicode for {@link Constants#TAU}.
    */
    public static final double τ = TAU;
}
